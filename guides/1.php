<h1>Guide til bestilling og betaling med Visa/Dankort</h1>

<div class="guide">

   <h2>Bestilling af en eller flere varer</h2>
   <p>Find en vare som du ønsker at bestille og tryk på ”Tilføj til kurv”:</p>
   <img src="../body/_gfx/guides/1/1.png"/>
   <p>Hvis du ønsker at ændre antallet af den tilføjede vare, ændre du tallet til det ønskede antal og trykker på ”Updade”:
   Hvis du vil fjerne en vare trykker du på ”Remove”:</p>
   <img src="../body/_gfx/guides/1/2.png"/>
   <p>Hvis du vil forsætte med at handle kan du tilføje flere vare, ved at trykke på ”Continue Shopping”:</p>
   <img src="../body/_gfx/guides/1/3.png"/>
   <p>Hvis du vil tilbage til indkøbskurven, så skal du klikke på ”Se Indkøbskurven”:</p>
   <img src="../body/_gfx/guides/1/4.png"/>
   
</div>

<div class="guide">

   <h2>Betaling af varer med Visa/Dankort</h2>
   <p>Hvis du ønsker at betale med Visa/Dankort eller andre betalingskort, skal du klikke på ”Check Out”:</p>
   <img src="../body/_gfx/guides/1/5.png"/>
   <p>Derefter skal du klikke på ”Don’t have a PayPal account?”:</p>
   <img src="../body/_gfx/guides/1/6.png"/>
   <p>Til sidst udfylder du fælderne og trykker på ”Review and Continue”:</p>
   <img src="../body/_gfx/guides/1/7.png"/>
   
</div>